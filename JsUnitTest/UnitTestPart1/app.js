function addNumbers(num1, num2){
    return num1 + num2 
}
function subtractNumbers(num1, num2){
    return num1 - num2 
}
function multiplyNumbers(num1, num2){
    return num1 * num2 
}
function divideNumber(num1, num2){
    return num1 / num2 
}

module.exports = {
    addNumbers, 
    subtractNumbers,
    multiplyNumbers,
    divideNumber,
}